#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char *str1;

	str1 = (char *)s;
	while (*str1 != c)
	{
		if (*str1 == '\0')
			return (NULL);
		str1++;
	}
	return (str1);
}
